# Comparison Operator
# entity > entity ======> Boolean =====> True or False

# <, >, <=, >=, ==, !=

print(4 < 5) # True
print(4 > 5) # False
print(4 >= 5) # False
print(4 <= 5) # True
print(4 == 5) # False
print(4 != 5) # True

print("--------------")

# Logical Operator

# and
# True and True =========> True
# True and False ========> False
# False and True ========> False
# False and False =======> False

print(5==5 and 4==4) # True
print(5==5 and 4!=4) # False
print(5!=5 and 4==4) # False
print(5!=5 and 4!=4) # False

print("------------")

# or
# True or True =======> True
# True or False ======> True
# False or True ======> True
# False or False =====> False

print(4==4 or 5==5) # True
print(4==4 or 5!=5) # True
print(4!=4 or 5==5) # True
print(4!=4 or 5!=5) # False

print("--------------")

# not
# not true =====> false
# not false ====> true

print(not 7 == 7) # False
print(not 7 == 8) # True

